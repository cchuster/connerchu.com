			<p class="title is-6">Construction of the Helmholtz Coil</p>
			<p class="subtitle is-6 is-spaced">
				A Helmholtz coil consists of two identical magnetic coils facing parallel to each other. The
				radius of each coil matches the distance separating the two coils. When current passes
				through both coils, it generates a uniform magnetic field. In this particular experiment,
				each coil had a radius of 50mm, each composed of 25 wounds of 14 American Wire Gauge
				insulated copper magnetic wire. One individual coil was attached to a power supply (Lavolta
				BPS-305) running at ~535mA and 0.05V, while the other to a waveform amplifier (Sony CMX-E22)
				and a signal generator (FG-100 DDS) (Figure 3). The intersection of the magnetic field
				produced by each coil summates to form a sinusoidal wave with a direct current offset.
			</p>
