			<p class="title is-4 is-spaced">Abstract</p>
			<p class="subtitle is-6 is-spaced">
				The demand for natural resources, and consequently greenhouse gas (GHG) emissions, are only
				set to increase in the next several decades. Methane gas, having 80 times more warming power
				than carbon dioxide over a 20 year period, accounts for about 10% of all U.S. GHG emissions
				primarily due to the agriculture and waste management industry. GHGs trap heat in Earth’s
				atmosphere causing rising surface temperatures and sea levels, ocean acidification, and
				extreme weather patterns. <i>Methylomicrobium alcaliphilum 20Z<sup>R</sup></i> is a
				methanotrophic bacterium currently being explored as a means for mitigating these emissions
				and/or production of value-added compounds from wasted sources of methane. Based on related
				studies, we predicted that exposure to low-frequency electromagnetic fields for 5 and 15
				minutes would be a feasible method to increase the growth rates of
				<i>M. alcaliphilum 20Z<sup>R</sup></i>, while 30 minutes would decrease the growth rates. We
				hypothesized that this increase in growth could make this method of catabolizing methane
				more practical given the slow growing nature of methanotrophs in standard conditions.
				However, our data showed that exposure to an electromagnetic field density of 0.1 milliTesla
				(mT) at a frequency of 50 hertz (Hz) for 5, 15, and 30 minutes had no statistically
				significant effects on the growth of <i>M. alcaliphilum 20Z<sup>R</sup></i> when compared to
				the control. The results of this study therefore do not fully support our hypothesis that an
				electromagnetic field can positively impact microbial growth, but future research should be done
				to further solidify and expand upon the data collected.
			</p>
			<p class="title is-4 is-spaced">Introduction</p>
			<p class="subtitle is-6 is-spaced">
				The methane concentration in Earth’s atmosphere has doubled since the Industrial Revolution,
				far exceeding natural levels, and accounts for about 20% of the warming in our planet (1).
				Given that methane is 80 times more potent in terms of heat capture than carbon dioxide, the
				effects that methane has on our environment are only magnified (1). In order to lessen this
				effect, researchers and environmentalists have recently taken particular interest in the use
				of methanotrophs, bacteria that consume methane as their source of energy for metabolism.
				Some byproducts of this unique metabolism can even be used for commercial and industrial
				production to produce biofuels and various chemicals like 2,3-butanediol for use as a
				precursor to plastics and pesticides (2). However, numerous studies have addressed the
				limitations associated with methanotrophic oxidation, including most commonly, the issue of
				slow bacterial growth rate, and thus slow methane uptake (3-5). Extremely low-frequency
				(ELF) electromagnetic fields (EMFs) are a form of non-ionizing, lowenergy, non-thermal
				radiation resulting from the combination of electric fields generated by differences in a
				circuit’s voltage and the magnetic fields generated by current flowing through a conductive
				medium (6-7). Several peer-reviewed studies quantifying the impacts of ELF EMFs on
				biological systems have shown that specific parameters like the frequency, time duration,
				and magnetic flux density (the number of magnetic field vectors passing through a given
				area) can accelerate normal cell potentials, increase the rate of healing, reduce
				inflammation, ameliorate bruising, and promote cell proliferation (8-11). Furthermore, these
				same positive impacts have also been observed on the growth of various types of bacteria
				(12-13). Optical density, used as a tool to quantify such growth, is a logarithmic intensity
				ratio of light absorption by a particular substance. If more light is absorbed, there exists
				a greater density of material as opposed to if less light was absorbed, thereby serving as a
				direct representation of growth over time.
				<br />
				<br />
				One of the most common exposure mechanisms in these studies is the use of the Helmholtz coil,
				which consists of two identical magnetic coils facing parallel to each other with the radius
				of each matching the distance separating them. As current passes through both coils, a uniform
				magnetic field is generated within the center of the structure, creating an ideal area for samples
				to be placed into. The positive characteristics of exposure, along with the well-documented methods
				for the construction and utilization of EMF exposure mechanisms like the Helmholtz coil configuration
				used in our study, show the vast potential for its implementation in a variety of industries
				(14-16).
				<br />
				<br />
				Here, we examine the effects of ELF EMFs on
				<i>Methylomicrobium alcaliphilum 20Z<sup>R</sup></i>, a halotolerant, alkaliphilic, obligate
				methanotrophic, Gram-negative bacterium isolated from moderately saline soda lakes in Tuva
				(Central Asia), with the aim of catalyzing species growth and consequently, methane
				consumption (17). We chose this strain specifically because of its tractable metabolic
				network and its versatility to a wide range of pHs and temperatures (2). The microbe’s
				ability to grow at higher salinities also helps to decrease contamination. After being
				exposed to 0.1mT 50Hz EMFs for 5, 15, and 30 minutes, we found that ELF EMFs do not have
				statistically significant effects on the growth of <i>M. alcaliphilum 20Z<sup>R</sup></i>.
				Despite the results not supporting our hypothesis, the premise of our study is still
				important as increasing the growth of such bacteria could have drastic impacts on reducing
				greenhouse gasses in our atmosphere.
			</p>
			<p class="title is-4 is-spaced">Results</p>
			<p class="subtitle is-6 is-spaced">
				Our experiment aimed to test the feasibility of 0.1mT 50 Hz ELF EMFs on increasing the
				growth of <i>M. alcaliphilum 20Z<sup>R</sup></i>. We divided the samples into four groups,
				each containing three bottles (biological replicates). The control group was not exposed to
				the EMF, while the three experimental groups were exposed for 5 minutes, 15 minutes, or 30
				minutes, with the exposure occurring at hour 20. We quantified the concentration (and
				therefore growth) of the bacteria by measuring the optical density (OD) of the samples
				periodically over 67 hours.
				<br />
				<br />
				The growth curve, which shows the OD reading as a function of time, suggests that at the end
				of exponential phase (hour 23.5), samples exposed to the EMF for 5 minutes had an approximate
				11.99% increase in growth when compared to the control group (Figure 1). Similarly, samples exposed
				for 15 minutes had an approximate 10.98% increase in growth, and those exposed for 30 minutes
				experienced around a 1.73% increase in growth over the control (Table 1). OD readings were taken
				well into the bacteria’s stationary phase in order to examine the longer lasting effects of this
				treatment. We observed that in each sample's highest OD reading at hour 45.5, those exposed for
				5 minutes had around a 16.79% higher maximum OD as compared to the control group. Additionally,
				those exposed for 15 minutes had an approximate 8.94% higher maximum density. On the other hand,
				the group exposed for a consecutive 30-minute time period experienced a 5.45% decrease in the
				maximum OD reading. It is important to note that the slight decrease in OD of the bacteria between
				hour 18 and 20 depicts the period in which samples were taken off the shaker whilst being exposed
				to the generated field.
