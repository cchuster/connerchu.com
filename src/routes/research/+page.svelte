			<p class="title is-4 is-spaced">Discussion</p>
			<p class="subtitle is-6 is-spaced">
				Methane is considered to be one of the most effective heattrapping greenhouse gasses (1).
				With the rise in emissions from open-pit landfills, farms, and factories, environmental
				scientists are scrambling for new methods to combat the release of this chemical. This study
				examines the effects of ELF EMFs on the growth of the methanotrophic bacterium <i
					>M. alcaliphilum 20Z<sup>R</sup></i
				>
				in hopes of stimulating bacterial proliferation but ultimately found that there were no statistically
				significant impacts.
				<br />
				<br />
				To this day, there have been mixed results on the effects of ELF EMF exposure on biological systems.
				Some studies have claimed these fields accelerate cell potentials, promote healing, and reduce
				inflammation, while others have noticed its detrimental effects on both bacterial colony forming
				unit count (the number of bacterial cells in a sample viable for binary fission) and OD readings
				(18-19). Perhaps the effects are dependent solely on the type of biological organism being exposed,
				or possibly there exists a complex relationship between the duration of time and EMF intensity
				a bacterial sample can be exposed to before causing negative/harmful effects (20). The wide range
				of positive and negative results generated by the experiments cited in this manuscript make it
				difficult to identify an exact reference point. Additionally, it is not known how large a change
				in growth could theoretically be, especially for each strain of bacteria, given the fact that
				research in this area on a molecular level is still novel.
				<br />
				<br />
				As our results indicate, exposure to a 0.1mT 50Hz ELF EMF for 5, 15, and 30 minutes does not
				appear to have a significant effect on the growth rates or growth curves of methanotrophic bacteria.
				However, this could very well be due to the low statistical power of this experiment, therefore
				making it important to continue experimentation under identical conditions. Some factors and
				limitations that could have influenced the results of this study was the fact that the ELF EMF
				exposure system was designed and developed in-house, meaning that small imperfections in the
				wounding of the wire may have had miniscule impacts on the perfect uniformity of the field created.
				<br />
				<br />
				The reason for choosing our specific parameters as outlined in our hypothesis was because of
				prior research done in the field with similar frequencies and intensities that demonstrated that
				exposure for 10 minutes could lead to positive effects on bacterial growth, enzyme kinetics,
				and gene expression, while exposure for longer periods of time greater than 30 minutes showed
				negative effects on these attributes (21-23). Future studies to expand on this one could test
				the effects of a larger range of field densities (0.1mT–5mT) and a larger range of exposure times
				(5–60 minutes). In short, similar and altered conditions with a larger sample size will help
				paint a better, more solid picture of the effects of ELF EMF exposure on methanotrophs and gain
				a stronger understanding of which variables could lead to the most positive effects.
				<br />
				<br />
				Although the results generated by this experiment supported the null hypothesis, the basis for
				this study is still important because successfully increasing the growth rates of methane-consuming
				bacteria can lead to an increase in methane oxidation rates and thus has the potential to reduce
				the detrimental methane concentration in Earth’s atmosphere.
