			<p class="title is-6">Preparation of Liquid Medium</p>
			<p class="subtitle is-6 is-spaced">
				The bacterium <i>M. alcaliphilum 20Z<sup>R</sup></i> was grown in a nitrate mineral salts
				(NMS) medium which contained 1M potassium nitrate (KNO<sub>4</sub>), 0.2M magnesium sulfate
				(MgSO<sub>4</sub>) x 7•H<sub>2</sub>O, 0.02M calcium dichloride (CaCl<sub>2</sub>) x 2•H<sub
					>2</sub
				>O, and distilled, deionized water (24). The medium was supplemented with 1mL/L trace
				solution, 20mL/L phosphate solution, and 40mL/L carbonate solution. The trace solution
				contained 5M ethylenediaminetetraacetic acid disodium salt (Na<sub>2</sub>EDTA), 2M ferrous
				sulfate (FeSO<sub>4</sub>) x 7•H<sub>2</sub>O, 0.3M zinc sulfate (ZnSO<sub>4</sub>) x 7•H<sub
					>2</sub
				>O, 0.03M manganese(II) chloride (MnCl<sub>2</sub>) x 4•H<sub>2</sub>O, 0.2M cobalt chloride
				(CoCl<sub>2</sub>) x 6•H<sub>2</sub>O, 0.6M copper sulfate (CuSO<sub>4</sub>) x 5•H<sub
					>2</sub
				>O, 0.05M nickel chloride (NiCl<sub>2</sub>) x 6•H<sub>2</sub>O, 0.05M sodium molybdate
				(Na2MoO4) x 2•H<sub>2</sub>O, and 0.03M boric acid (H<sub>3</sub>BO<sub>3</sub>). The
				phosphate solution contained 5.44M potassium dihydrogen phosphate (KH<sub>2</sub>PO<sub
					>4</sub
				>) and 5.68M disodium hydrogen phosphate (Na<sub>2</sub>HPO<sub>4</sub>) while the carbonate
				solution contained 75.6M sodium bicarbonate (NaHCO<sub>3</sub>), and 10.5M sodium carbonate
				(Na<sub>2</sub>CO<sub>3</sub>).
			</p>
			<p class="title is-6">Preparation of Liquid Culture</p>
			<p class="subtitle is-6 is-spaced">
				Twelve 100mL bottles were autoclaved at 121°C for 45 minutes. In order to increase the pH
				and use it as a buffer, 3mL of phosphate and 6mL of carbonate solutions were then added into
				an already prepped bottle containing 150mL of NMS medium. Once mixed, 25mL of this solution
				was then inserted into three bottles for three biological replicate samples. Cultures were
				inoculated with bacteria grown from a frozen stock on a petri dish containing NMS agar. 50cm<sup
					>3</sup
				>
				of gaseous methane via needle and syringe was introduced into each bottle. The prepared containers
				were then placed in the 30°C shaker overnight at 200rpm. Once again, 3mL of phosphate and 6mL
				of carbonate were then each added into two new 150mL bottles of NMS medium and then divided evenly
				(25mL each) into the 12 previously autoclaved 100mL bottles. They were then labeled based on
				their associated biological replicates and time of exposure (control or 0 minutes, 5 minutes,
				15 minutes, or 30 minutes). OD readings were taken of the three initial biological replicate
				bottles at 600nm. All 12 cultures were then split to OD = 0.024. It was determined that 0.67mL
				of biological replicate 1, 0.66mL of biological replicate 2, and 0.75mL of biological replicate
				3 was needed to obtain these concentrations. After inoculation of the bacteria into the bottles,
				50cm<sup>3</sup> of methane gas was then added to each of the 12 bottles via needle and syringe
				and then placed in the 30°C shaker overnight until it reached an OD of 0.50 the next morning.
			</p>
			<p class="title is-6">ELF EMF Exposure</p>
			<p class="subtitle is-6 is-spaced">
				Each sample was then exposed to the ELF EMF for the specified duration of time just before
				reaching the exponential phase (hour 20). In order to maintain a controlled experiment, all
				bottles were taken off the shaker for the same amount of time, meaning those not facing
				exposure at the moment were placed on the side away from the generating source.
			</p>
			<p class="title is-6">Optical Density Readings</p>
			<p class="subtitle is-6 is-spaced">
				OD readings were taken every 1–2 hours apart until hour 67. A needle was inserted into the
				lid of each bottle and 1mL of solution was extracted. The liquid was then placed in a
				spectrophotometer (Jenway 6320D) at 600nm and values were recorded on a table.
			</p>
			<p class="title is-6">Data Analysis</p>
			<p class="subtitle is-6 is-spaced">
				The average values for the three biological replicates were taken and used to calculate
				growth, maximum optical density, and growth rate along with the respective percent change.
				Growth is simply the average OD values recorded at each period in time. By subtracting the
				OD value of the control at the end of exponential phase (hour 23.5) from each of the exposed
				samples, dividing it by the control, and then multiplying by 100, the percent difference can
				be seen. A similar process was done for maximum OD except at hour 45.5. Growth rate for each
				condition was calculated from the moment of exposure at hour 20 to the end of exponential
				phase at hour 23.5 using the exponential growth equation Nt = No (1 + r)t , where Nt is the
				final OD value, No is the initial OD value, r as the unknown rate constant, and t as time.
				The percent difference equation was applied to the newfound r values in an identical fashion
				to the growth and optical density calculations. The built-in Microsoft Excel one-tailed
				t-test was used to calculate significance between the control and exposed samples. To
				account for multiple comparisons a Bonferroni corrected α-level of 0.016 was used.
			</p>
